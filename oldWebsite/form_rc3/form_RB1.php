<script type="text/javascript">
    $('.loc-prop-item-list li').click(function() {
        $('.loc-prop-selected').removeClass('loc-prop-selected');
        $(this).addClass('loc-prop-selected')
    });
    function jdfTog(_this, sel) {
        var tmp = $(_this);
        if(tmp.is(':checked')) {
            $(sel).attr('readonly','readonly');
            $(sel).parent().addClass('jd-field-readonly');
        } else {
            $(sel).parent().removeClass('jd-field-readonly');
            $(sel).attr('readonly','');
        }

    }

    function chkFieldOpt(_this, sel) {
        if($(_this).val() == '-1') {
            $(sel).show().find('input[type=text]').focus();
        } else {
            $(sel).hide();
        }
    }

    function chkFieldOpt2(_this,sel) {
        if($(_this).is(':checked')) {
            $(sel).show().find('input[type=text]').focus();
            $(sel).prev('.jd-opt-sub-label').css('display','block');
        } else {
            $(sel).hide();
            $(sel).prev('.jd-opt-sub-label').hide();
        }
    }
</script>
